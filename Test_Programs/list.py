# A list is a collection of ordered items, list are mutable, list can contain heterogenous items.

teams = ["barca", "madrid", "city", "chelsea", "united", "bayern"]

print(teams)

teams[2] = "psg"
print(teams)
teams.pop()
print(teams)
teams.append("juve")
print(teams)
teams.insert(2,"new castle")
print(teams)
teams.remove("madrid")
print(teams)
teams.reverse()
print(teams)
teams.sort()
print(teams)